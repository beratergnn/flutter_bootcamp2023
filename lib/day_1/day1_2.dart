void main() {
  print('Hello World!');

  // Değişken Tanımlama Tip Belirmeksizin
  var yas = 34;
  print(yas);
  // Değişken Tanımlama Tip Beliterek
  int sayi = 34;
  print(sayi);
  // Primitive Tipler

  // Tam Sayılar = int, Ondalıklı Sayılar = double
  // Metinsel İfadeler = String, Mantıksal İfadeler = bool

  // Değişken İsimlendirme Kuralları;
